let canvas = document.querySelector("#canvas")
let ctx = canvas.getContext("2d")
let tr = document.querySelector("#translate").getContext("2d")
let rot = document.querySelector("#rotate").getContext("2d")
let sc = document.querySelector("#scale").getContext("2d")
let image = document.querySelector("img")
image.addEventListener('load', e=>{
    ctx.drawImage(image, 0, 0, 1000, 666, 0, 0, 300, 200)
    ctx. fillRect(300, 0, 100, 100)
    tr.translate(100, 100)
    tr.drawImage(image, 0, 0, 1000, 666, 0, 0, 300, 200)
    tr.fillRect(300, 0, 100, 100)
    rot.rotate(Math.PI/6)
    rot.drawImage(image, 0, 0, 1000, 666, 0, 0, 300, 200)
    rot.fillRect(300, 0, 100, 100)
    sc.scale(0.5, 1.5)
    sc.drawImage(image, 0, 0, 1000, 666, 0, 0, 300, 200)
    sc.fillRect(300, 0, 100, 100)
    }
)
